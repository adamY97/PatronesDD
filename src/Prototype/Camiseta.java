package Prototype;

public class Camiseta {

	protected String nombre;
	protected String talla;
	protected String color;
	protected String material;
	
	public Camiseta(String nombre, String talla, String color, String material) {
		
		this.nombre = nombre;
		this.talla = talla;
		this.color = color;
		this.material = material;
	}
}
